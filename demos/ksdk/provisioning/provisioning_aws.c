static sss_status_t add_key(sss_object_t *keyObject,
    sss_key_part_t keyPart,
    sss_cipher_type_t cipherType,
    uint8_t *buff,
    size_t buff_size,
    size_t keyBitLen,
    uint32_t keyId,
    uint32_t options,
    sss_key_store_t *pKs)
{
    sss_status_t status = kStatus_SSS_Success;
    uint8_t publicIndex = 0, privateIndex = 0;
    size_t pubLen = 0, privLen = 0;
    uint8_t privKeyBuf[40] = {0};

    /* Parse keypair to get private key */

    if (ex_util_asn1_get_ec_pair_key_index(buff, buff_size, &publicIndex, &pubLen, &privateIndex, &privLen) != 0) {
        status = kStatus_SSS_Fail;
        goto exit;
    }
    ENSURE_OR_EXIT_WITH_STATUS_ON_ERROR(privateIndex < buff_size, status, kStatus_SSS_Fail);
    ENSURE_OR_EXIT_WITH_STATUS_ON_ERROR(privLen < sizeof(privKeyBuf), status, kStatus_SSS_Fail);
    memcpy(&privKeyBuf[0], &buff[privateIndex], privLen);

    status = sss_key_object_init(keyObject, pKs);
    if (status != kStatus_SSS_Success) {
        LOG_I(" sss_key_object_init Failed ");
        goto exit;
    }

    keyId  = keyId & 0xFF;
    status = sss_key_object_allocate_handle(keyObject, keyId, keyPart, cipherType, privLen, options);
    if (status != kStatus_SSS_Success) {
        LOG_E(" sss_key_object_allocate_handle Failed ");
        goto exit;
    }

    status = sss_key_store_set_key(pKs, keyObject, &privKeyBuf[0], privLen, keyBitLen, NULL, 0);
    if (status != kStatus_SSS_Success) {
        LOG_E(" sss_key_store_set_key Failed ");
        goto exit;
    }
exit:
    return status;
}

static sss_status_t add_cert(uint32_t keyId, const uint8_t *buff, size_t buff_size, sss_nx_session_t *nx_session)
{
    sss_status_t status                   = kStatus_SSS_Fail;
    smStatus_t sm_status                  = SM_NOT_OK;
    uint8_t fileNo                        = 0;
    uint16_t isoFileID                    = 0;
    uint8_t fileOption                    = Nx_CommMode_NA;
    uint8_t fileReadAccess                = Nx_AccessCondition_Free_Access;
    uint8_t fileWriteAccess               = Nx_AccessCondition_Free_Access;
    uint8_t fileReadWriteAccess           = Nx_AccessCondition_Free_Access;
    uint8_t fileChangeAccess              = Nx_AccessCondition_Free_Access;
    size_t writeOffset                    = 0;
    size_t i                              = 0;
    uint8_t fIDList[NX_FILE_ID_LIST_SIZE] = {0};
    size_t fIDListLen                     = 0;
    bool fileExists                       = false;

    fileNo    = (uint8_t)(keyId & 0xFF);
    isoFileID = (fileNo + 1);

    /* Check for the file exists */
    sm_status = nx_GetFileIDs(&nx_session->s_ctx, fIDList, &fIDListLen);
    ENSURE_OR_GO_EXIT(fIDListLen <= NX_FILE_ID_LIST_SIZE);
    ENSURE_OR_GO_EXIT(sm_status == SM_OK);

    for (i = 0; i < fIDListLen; i++) {
        if (fileNo == fIDList[i]) {
            fileExists = true;
            break;
        }
    }

    if (fileExists == false) {
        sm_status = nx_CreateStdDataFile(&nx_session->s_ctx,
            fileNo,
            isoFileID,
            fileOption,
            buff_size,
            fileReadAccess,
            fileWriteAccess,
            fileReadWriteAccess,
            fileChangeAccess);
        ENSURE_OR_GO_EXIT(sm_status == SM_OK);
        LOG_I("Certificate Data File creation successful !!!");
    }
    else {
        LOG_I("Certificate Data File already exist !!!");
    }

    sm_status = nx_WriteData(&nx_session->s_ctx, fileNo, writeOffset, buff, buff_size, Nx_CommMode_NA);

    if (sm_status != SM_OK) {
        LOG_E("Failed to write data");
        goto exit;
    }
    status = kStatus_SSS_Success;
exit:
    return status;
}

sss_status_t ex_sss_entry(ex_sss_boot_ctx_t *pCtx)
{
    sss_status_t status;
    sss_object_t obj_kp = {0};

    status = add_key(&obj_kp,
        kSSS_KeyPart_Pair,
        kSSS_CipherType_EC_NIST_P,
        client_key,
        sizeof(client_key),
        256,
        SSS_KEYPAIR_INDEX_CLIENT_PRIVATE,
        kKeyObject_Mode_Persistent,
        &pCtx->ks);
    if (status != kStatus_SSS_Success) {
        LOG_E("key pair provisioning failed ... \n");
        goto exit;
    }

    status = add_cert(SSS_CERTIFICATE_INDEX_CLIENT, client_cer, sizeof(client_cer), (sss_nx_session_t *)&pCtx->session);
    if (status != kStatus_SSS_Success) {
        LOG_E(" Client certificate failed... \n");
        goto exit;
    }

    LOG_I(" PROVISIONING SUCCESSFUL!!!");
exit:
    LOG_I("Provisioning Example Finished");
    return status;
}
